function solution(n) {
    // 입력된 숫자 n을 문자열로 변환합니다.
    // 예: n = 123 → "123"
    return (n + '') 
        // 문자열을 문자 단위로 분리하여 배열로 만듭니다.
        // 예: "123" → ['1', '2', '3']
        .split('')  
        // 배열의 순서를 뒤집습니다.
        // 예: ['1', '2', '3'] → ['3', '2', '1']
        .reverse()  
        // 배열의 각 요소를 정수로 변환합니다.
        // 예: ['3', '2', '1'] → [3, 2, 1]
        .map(n => parseInt(n)); 
}

// 예제 1: 입력 숫자 123
console.log(solution(123)); // 출력: [3, 2, 1]

// 예제 2: 입력 숫자 4567
console.log(solution(4567)); // 출력: [7, 6, 5, 4]

// 예제 3: 입력 숫자 0
console.log(solution(0)); // 출력: [0]

// 예제 4: 입력 숫자 987654
console.log(solution(987654)); // 출력: [4, 5, 6, 7, 8, 9]
